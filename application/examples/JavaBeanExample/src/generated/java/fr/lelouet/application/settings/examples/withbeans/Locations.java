package fr.lelouet.application.settings.examples.withbeans;

public class Locations {
    private String a = new String();
    private double b;

    /**
     * @return
     *     the {@link #a}
     */
    public String getA() {
        return a;
    }

    /**
     * set the {@link #a}
     */
    public void setA(String value) {
        this.a = value;
    }

    /**
     * @return
     *     the {@link #b}
     */
    public double getB() {
        return b;
    }

    /**
     * set the {@link #b}
     */
    public void setB(double value) {
        this.b = value;
    }
}
